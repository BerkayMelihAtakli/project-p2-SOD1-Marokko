    <h1>Doelstelling van de landenopdracht</h1>
    <br>
    <p>
      Met de landenopdracht laten we zien dat ICT niet alleen “techniek om de techniek” is, maar ook een direct maatschappelijk
      belang kan dienen. In de presentatie van dhr. Valies en in het document van de landenopdracht wordt duidelijk dat we als team
      leren om informatie te onderzoeken, te selecteren en vervolgens toegankelijk te maken voor een doelgroep. Dat is precies een
      vaardigheid waar je als ICT’er in de praktijk vaak mee te maken krijgt: je bouwt niet zomaar een website of applicatie, je
      helpt mensen om betere keuzes te maken door informatie duidelijk, betrouwbaar en bruikbaar te presenteren.
    </p>
    <br>
    <p>
      Voor ons land focussen we op een groot maatschappelijk probleem in Marokko: jeugdwerkloosheid en de groep jongeren die niet in
      onderwijs, werk of training zit. In veel analyses wordt dit gezien als een kernprobleem, omdat het niet alleen gaat om
      geld verdienen, maar ook om kansen, toekomstperspectief en sociale stabiliteit. Als grote groepen jongeren langdurig geen baan
      of opleiding hebben, ontstaat er frustratie en groeit de druk om te migreren of om informele (onzekere) banen te accepteren.
      Dit probleem hangt bovendien samen met andere thema’s zoals ongelijkheid tussen regio’s (stad/platteland), aansluiting van
      onderwijs op de arbeidsmarkt en beperkte doorstroom naar veilige, vaste banen.
    </p>
    <br>
    <p>
      De doelstelling van onze landenopdracht is daarom tweeledig. Ten eerste willen we inzicht geven in de context van Marokko:
      hoe ziet de samenleving eruit, hoe zijn economie en onderwijs georganiseerd, en waarom is jeugdwerkloosheid zo hardnekkig?
      Ten tweede vertalen we die context naar een ICT-bril: welke digitale oplossingen of informatievoorziening kan bijdragen aan
      meer kansen? Denk aan het beter matchen van jongeren en stages, het inzichtelijk maken van lokale opleidingen en certificaten,
      of het aanbieden van praktische “skills routes” die duidelijk maken welke vaardigheden je nodig hebt voor bepaalde functies.
      ICT kan ook helpen om drempels te verlagen: online leren, portfolio’s bouwen, sollicitatiehulp, of zelfs remote-work routes
      waarbij jongeren buiten hun eigen regio kansen vinden.
    </p>
    <br>
    <p>
      In de aanpak van ons team betekent dit concreet dat we (1) bronnen verzamelen en beoordelen, (2) kerninformatie samenvatten in
      begrijpelijke taal, (3) die informatie structureren op de website met duidelijke pagina’s (zoals deze doelstelling-pagina),
      en (4) de gebruiker helpen met navigatie via een vaste header, identiek menu en dezelfde footer op elke pagina. Daarmee oefenen
      we niet alleen front-end technieken (HTML/CSS/JS), maar ook professionele werkwijzen: consistent design, herbruikbare onderdelen,
      en een heldere informatie-architectuur. Dat sluit aan bij wat je later in projecten doet: samenwerken, versiebeheer (Git/GitHub),
      en stap voor stap verbeteren op basis van feedback.
    </p>
    <br>
    <p>
      Uiteindelijk willen we met deze landenopdracht laten zien dat ICT’ers maatschappelijke waarde toevoegen door problemen te
      begrijpen en oplossingen bruikbaar te maken. Jeugdwerkloosheid is een complex vraagstuk dat niet “met één website” verdwijnt,
      maar goede digitale informatie en slimme matching kunnen wél bijdragen aan betere kansen, meer transparantie en meer
      zelfredzaamheid. Onze website is dus zowel een leerproject voor ons als team, als een voorbeeld van hoe je ICT kunt inzetten
      om maatschappelijke thema’s zichtbaar en toegankelijk te maken. 
    </p>
